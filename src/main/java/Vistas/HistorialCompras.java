    private void llenarTablaCompras() {
    	List<CompraDTO> comprasUsuario = controladorHistorialCompras.obtenerComprasPorUsuario(usuario.getId_usuario());
		
    	try {
    		DefaultTableModel model = new DefaultTableModel();
    		model.setColumnIdentifiers(new Object[] { "NÂ°", "fecha", "Pelicula", "Cant. boletas", "Total Compra"});
    		for (int i = 0; i < comprasUsuario.size(); i++) {
    			CompraDTO compra = comprasUsuario.get(i);
    			BoletaDTO infoBoleta = controladorHistorialCompras.obtnerInfoBoletaCompraPorUsuarioCompra(compra.getIdCompra());
    			PeliculaDTO pelicula = controladorHistorialCompras.obtenerPelicula(infoBoleta.getId_funcion());
    			model.addRow(new Object[] { 
    					i+1,
    					compra.getFecha(),
    					pelicula.getNombre(),
    					compra.getCantidad(),
    					compra.getCantidad() * infoBoleta.getPrecio()
    			});
    		}
    		tablaCompras.setModel(model);
    	}catch(SQLException | PeliculaNoEncontradaException | FuncionNoEncontradaException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar compras: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    	}

    }

