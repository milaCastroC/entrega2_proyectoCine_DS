    public void llenarCbPeliculas() {
    	try {
    		List<PeliculaDTO> peliculas = controladorVentanaPeliculasCliente.obtenerPeliculas();
    		cbPeliculas.removeAllItems();
    		cbPeliculas.addItem("--Seleccione una pelicula--");
            for (int i = 0; i < peliculas.size(); i++) {
            	PeliculaDTO pelicula = peliculas.get(i);
                cbPeliculas.addItem(pelicula);
            }
    	}catch(SQLException e) {
    		e.printStackTrace();
    	}
    }
    
    private void llenarListaFunciones(int idPelicula) {
    	try {
    		List<FuncionDTO> funciones = controladorVentanaPeliculasCliente.obtenerFuncionesPorPelicula(idPelicula);
    		DefaultListModel<String> modelo = new DefaultListModel<>();
            
            if(!funciones.isEmpty()){
            	for (FuncionDTO funcion : funciones) {
                    modelo.addElement(funcion.toString());
                }
            } else {
            	modelo.addElement("NO HAY FUNCIONES DISPONIBLES");
            }
            
            listaFunciones.setModel(modelo);
		} catch (Exception e) {
			e.printStackTrace();
		}
    }
    
    private void mostrarAgregarComentario(int idPelicula) {
    	if(controladorVentanaPeliculasCliente.peliculaVista(idPelicula, usuario.getId_usuario())) {
    		panelAgregarComentario.setVisible(true);
    	} else {
    		panelAgregarComentario.setVisible(false);
    	}
    }
    
    private void cargarComentarios(int idPelicula) {
    	List<ComentarioDTO> comentarios = controladorVentanaPeliculasCliente.obtenerComentariosPorPelicula(idPelicula);
    	
    	String textoComentarios = "";
    	for(ComentarioDTO i: comentarios) {
    		String estructuraComentario = "";
    		
    		UsuarioDTO usuario = controladorVentanaPeliculasCliente.obtenerUsuarioPorId(i.getIdUsuario());
    		estructuraComentario += usuario.getNombre() + " - " + i.getFecha() + "\n";
    		estructuraComentario += i.getContenido() + "\n\n --------------------------------------------------------------\n\n";
    		textoComentarios += estructuraComentario;
    	}
    	taComentarios.setText(textoComentarios);
    	
        taComentarios.setCaretPosition(0); // Desplazar al inicio
    }
    
