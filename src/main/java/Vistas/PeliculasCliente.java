    private void cbPeliculasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbPeliculasItemStateChanged
        if(cbPeliculas.getSelectedIndex() != 0 && cbPeliculas.getSelectedIndex() != -1){
        	PeliculaDTO pelicula = (PeliculaDTO)cbPeliculas.getSelectedItem();
        	labelNombrePeli.setText(pelicula.getNombre());
        	labelDirector.setText(pelicula.getDirector());
        	labelDuracion.setText(pelicula.getDuracion_minutos() + " min.");
        	labelGenero.setText(pelicula.getGenero());
        	taSinopsis.setText(pelicula.getSipnosis());
        	llenarListaFunciones(pelicula.getId_pelicula());
        	mostrarAgregarComentario(pelicula.getId_pelicula());
        	cargarComentarios(pelicula.getId_pelicula());
        } else {
            limpiarCampos();
        }
    }//GEN-LAST:event_cbPeliculasItemStateChanged

    private void btnAgregarComentarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarComentarioActionPerformed
        String contenido = taAgregarComentarios.getText();
        if(!contenido.isEmpty()) {
        	LocalDate fecha = LocalDate.now();
            java.sql.Date sqlDate = java.sql.Date.valueOf(fecha);
            PeliculaDTO pelicula = (PeliculaDTO)cbPeliculas.getSelectedItem();
        	ComentarioDTO comentario = new ComentarioDTO(0, contenido, usuario.getId_usuario(), sqlDate, pelicula.getId_pelicula());
        	
        	controladorVentanaPeliculasCliente.agregarComentario(comentario);
            
        	JOptionPane.showMessageDialog(this, "Comentario subido éxitosamente");
        	taAgregarComentarios.setText("");
        	cargarComentarios(pelicula.getId_pelicula());
        	
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, rellena todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnAgregarComentarioActionPerformed

    
    
    
