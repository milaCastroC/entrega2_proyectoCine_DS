    private void btnAgregarSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarSalaActionPerformed
    	String codigo = txtCodigoSala.getText();
    	String nombre = txtNombreSala.getText();
    	int capacidad = (int)sCapacidad.getValue();
    	
    	if(validarCampos()) {
    		try {
    			SalaDTO sala = new SalaDTO(0, codigo, nombre, capacidad);
        		controladorGestionSalas.agregarSala(sala);
        		
        		llenarTabla();
        		limpiarCampos();
        		JOptionPane.showMessageDialog(null, "Sala creada con éxito");
    		}catch(CodigoRepetidoException | CapacidadNoValidaException e) {
                JOptionPane.showMessageDialog(this, "Error al agregar sala: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    		}
    	} else {
            JOptionPane.showMessageDialog(this, "Por favor, rellena todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
    	}
    }//GEN-LAST:event_btnAgregarSalaActionPerformed

    private void btnEliminarSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarSalaActionPerformed
    	String codigo = txtCodigoSala.getText();
    	if(!codigo.isEmpty()) {
    		try {
    			controladorGestionSalas.eliminarSala(codigo);
        		llenarTabla();
        		limpiarCampos();
        		JOptionPane.showMessageDialog(null, "Sala eliminada con éxito");

    		}catch(SalaNoEncontradaException e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar sala: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    		}
    	} else {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el código para buscar", "Advertencia", JOptionPane.WARNING_MESSAGE);
    	}
    }//GEN-LAST:event_btnEliminarSalaActionPerformed

    private void btnEditarSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarSalaActionPerformed
    	String codigo = txtCodigoSala.getText();
    	if(validarCampos()) {
    		try {
    			SalaDTO sala = controladorGestionSalas.buscarPorCodigo(codigo);
        		String nuevoNombre = txtNombreSala.getText();
        		int capacidad = (int)sCapacidad.getValue();
        		
        		SalaDTO salaEditada = new SalaDTO(sala.getId_sala(), codigo, nuevoNombre, capacidad);
        		controladorGestionSalas.actualizarSala(salaEditada);
        		llenarTabla();
        		limpiarCampos();
        		JOptionPane.showMessageDialog(null, "Sala actualizada con éxito");

    		} catch(SalaNoEncontradaException | CapacidadNoValidaException e) {
                JOptionPane.showMessageDialog(this, "Error al editar sala: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    		}
    	} else {
            JOptionPane.showMessageDialog(this, "Por favor, rellena todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
    	}
    }//GEN-LAST:event_btnEditarSalaActionPerformed

