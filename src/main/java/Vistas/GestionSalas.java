    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    	String codigo = txtCodigoSala.getText();
    	if(!codigo.isEmpty()) {
    		try {
    			SalaDTO sala = controladorGestionSalas.buscarPorCodigo(codigo);
        		txtNombreSala.setText(sala.getNombreSala());
        		sCapacidad.setValue(sala.getCapacidad());
    		}catch(SalaNoEncontradaException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    		}
    	} else {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el código para buscar", "Advertencia", JOptionPane.WARNING_MESSAGE);
    	}
    }//GEN-LAST:event_btnBuscarActionPerformed

    
    private void llenarTabla() {
    	List<SalaDTO> listaSalas = controladorGestionSalas.getSalas();
    	DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "Código", "Nombre", "Capacidad"
        });
        for (int i = 0; i < listaSalas.size(); i++) {
            SalaDTO sala = listaSalas.get(i);
            model.addRow(new Object[]{
                sala.getCodigoSala(),
                sala.getNombreSala(),
                sala.getCapacidad()
            });
        }
        tablaSalas.setModel(model);
    }
    
    private boolean validarCampos() {
    	String codigo = txtCodigoSala.getText();
    	String nombre = txtNombreSala.getText();
    	int capacidad = (int)sCapacidad.getValue();
    	
    	if(codigo.isEmpty() | nombre.isEmpty() ) {
    		return false;
    	}
    	return true;
    }
    
    private void limpiarCampos() {
    	txtCodigoSala.setText("");
    	txtNombreSala.setText("");
    	sCapacidad.setValue(0);
    }

